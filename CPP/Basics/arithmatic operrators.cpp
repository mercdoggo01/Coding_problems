#include<iostream>
using namespace std;
int main()
{
int a=4 ,b=5;
cout<<"operator in c++"<< endl;
cout<<"the value of a + b is "<< a+b <<endl ;
cout<<"the value of a - b is "<< a-b <<endl ;
cout<<"the value of a * b is "<< a*b <<endl ;
cout<<"the value of a / b is "<< a/b <<endl ;
cout<<"the value of a ++ is " << a++ <<endl ;
cout<<"the value of a-- is " << a-- <<endl ;
cout<<"the value of ++a is " << ++a <<endl ;
cout<<"the value of --a is " << --a <<endl ;
return 0;
}